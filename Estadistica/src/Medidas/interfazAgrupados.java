    public void media() {
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        int rows = tabla.getRowCount();
        modelo.setColumnCount(4);
        modelo.addColumn("MCi*fi");
        for (int i = 0; i < rows - 1; i++) {
            double frecuencia = ag.getMcf(i);
            tabla.setValueAt(frecuencia, i, 4);
        }
        tabla.setValueAt(ag.sumaMcfi(), rows - 1, 4);
        txtShowForm.setIcon(formula.getIconLaTex(ag.mostrarMediaAgrupado()));
    }

    public double varianza() {
         DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        int rows = tabla.getRowCount();
        modelo.setColumnCount(4);
        modelo.addColumn("MCi*fi");
        for (int i = 0; i < rows - 1; i++) {
            double frecuencia = ag.getMcf(i);
            tabla.setValueAt(frecuencia, i, 4);
        }
        double sumMcf = ag.sumaMcfi();
        double N = ag.getN();
        tabla.setValueAt(sumMcf, rows - 1, 4);
        double media = sumMcf / N;
        modelo.addColumn("(mc-X)^2");
        modelo.addColumn("(mc-X)^2 *f");
        double acum = 0;
        for (int i = 0; i < rows - 1; i++) {
            double mc = (double) tabla.getValueAt(i, 2);
            double suma = Math.pow((mc - media), 2);
            tabla.setValueAt(suma, i, 5);
            double frecuencia = ag.getFrecuencia(i);
            double suma2 = suma * frecuencia;
            tabla.setValueAt(suma2, i, 6);
            acum += suma2;
        }
        tabla.setValueAt(acum, rows - 1, 6);
        txtShowForm.setIcon(formula.getIconLaTex(ag.mostrarVarianzaAgrupado(acum)));
        return acum;
    }

