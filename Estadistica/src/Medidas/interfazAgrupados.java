    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel dt = (DefaultTableModel) tabla.getModel();
        int cantIntervalo = Integer.parseInt(txtCantInt.getText());
        dt.setRowCount(cantIntervalo + 1);
        ag = new Agrupado(cantIntervalo, (String) cbEstado.getSelectedItem());
        double inicio = Double.parseDouble(txtFirst.getText());
        double ultimo = Double.parseDouble(txtSecond.getText());
        int amplitud = (int) Math.floor((ultimo - inicio) / cantIntervalo);
        for (int i = 0; i < cantIntervalo; i++) {
            double limitederecho = amplitud + inicio;
            double marcaClase = (limitederecho + inicio) / 2;
            ag.setLimiteInferior(inicio, i);
            ag.setLimiteSuperior(limitederecho, i);
            tabla.setValueAt(inicio, i, 0);
            tabla.setValueAt(limitederecho, i, 1);
            tabla.setValueAt(marcaClase, i, 2);
            inicio = limitederecho;
        }
        dt.setColumnCount(4);
    }//GEN-LAST:event_jButton1ActionPerformed

