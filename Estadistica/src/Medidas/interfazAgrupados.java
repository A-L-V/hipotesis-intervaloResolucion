    private void cbActividadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbActividadActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        int rows = tabla.getRowCount();
        modelo.setColumnCount(4);
        switch ((String) cbActividad.getSelectedItem()) {
            case "media":
                media();
                break;
            case "mediana":
                modelo.addColumn("F: frecuencia acumulada");
                double acumulado = 0;
                for (int i = 0; i < rows - 1; i++) {
                    acumulado += ag.getFrecuencia(i);
                    tabla.setValueAt(acumulado, i, 4);
                }
                txtShowForm.setIcon(formula.getIconLaTex(ag.mostrarMedianaAgrupado()));
                break;
            case "moda":
                txtShowForm.setIcon(formula.getIconLaTex(ag.mostrarModaAgrupado()));
                break;
            case "varianza":
                varianza();
                break;
            case "desviacion estandar":
                txtShowForm.setIcon(formula.getIconLaTex(ag.mostrarDesviacionAgrupado(varianza())));
                break;
            case "coeficiente de variacion":
                txtShowForm.setIcon(formula.getIconLaTex(ag.mostrarCoeficienteAgrupado(varianza())));
                break;
            case "percentil":
                modelo.addColumn("F: frecuencia acumulada");
                double acum = 0;
                for (int i = 0; i < rows - 1; i++) {
                    acum += ag.getFrecuencia(i);
                    tabla.setValueAt(acum, i, 4);
                }
                double percentil = Double.parseDouble(JOptionPane.showInputDialog("poner percentil en porcentaje"));
                txtShowForm.setIcon(formula.getIconLaTex(ag.mostrarPercentilAgrupado(percentil / 100)));
                break;
        }
    }//GEN-LAST:event_cbActividadActionPerformed

